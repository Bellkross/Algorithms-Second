public class Point {
    private int i;
    private int j;

    public int i() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int j() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }

    public Point(int i, int j) {
        this.i = i;
        this.j = j;
    }
}
